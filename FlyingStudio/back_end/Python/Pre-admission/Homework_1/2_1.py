#!/usr/bin/python3

import turtle

turtle.screensize(canvwidth=800, canvheight=600, bg="blue")
turtle.setup(width=800,height=600, startx=0.4, starty=0.3)
turtle.pencolor("red")
turtle.forward(25)
turtle.right(90)
turtle.forward(50)
turtle.right(90)
turtle.forward(100)
turtle.right(90)
turtle.forward(200)
turtle.right(90)
turtle.forward(400)
turtle.home()
turtle.circle(100)